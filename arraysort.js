function arraySort(anArray) {
    if (anArray%2===0){
        return (anArray.push([evens]));
        return ("Evens");
    }
    else if (anArray%2!==0){
        return (anArray.push([odds]));
        return ("Odds");
    }
    else{
        return (anArray.push([chars]));
        return ("Chars");
    }

}
let anArray=[2,0,6,5,1,7,'z','a'];
let evens=new Array();
let odds=new Array();
let chars=new Array();

module.exports = arraySort;



